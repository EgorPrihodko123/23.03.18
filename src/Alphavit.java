public class Alphavit {
    public int num;
    public char letter;

    public Alphavit(int num, char letter) {
        this.num = num;
        this.letter = letter;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public char getLetter() {
        return letter;
    }

    public void setLetter(char letter) {
        this.letter = letter;
    }
}
